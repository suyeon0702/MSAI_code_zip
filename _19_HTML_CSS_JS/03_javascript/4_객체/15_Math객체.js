console.log(Math.abs(-10)); // 절댓값: 10
console.log(Math.pow(2, 3)); // 거듭제곱: 8
console.log(Math.sqrt(9)); // 제곱근: 3
console.log(Math.ceil(4.2)); // 내림: 4
console.log(Math.floor(4.8)); // 반올림: 5
console.log(Math.round(4.5)); // JS에서의 round는 소숫점 자리 설정 기능은 없다.
console.log(Math.max(1, 2, 3)); // 최댓값: 3
console.log(Math.min(1, 2, 3)); // 최솟값: 1
console.log(Math.random()); // 난수 생성(0이상 1미만) // Python의 randint 같은 기능은 없다
console.log(Math.PI); // 원주율: 3.14